/**
 * Frequency that an event recurs.
 */
export enum EventRecurrence {

	/**
	 * Annually.
	 */
	Annually = "annually",

	/**
	 * Daily.
	 */
	Daily = "daily",

	/**
	 * Monthly.
	 */
	Monthly = "monthly",

	/**
	 * One time.
	 */
	Once = "once",

	/**
	 * Quarterly.
	 */
	Quarterly = "quarterly",

	/**
	 * Weekly.
	 */
	Weekly = "weekly"
}
